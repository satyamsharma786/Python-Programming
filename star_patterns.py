#square of * using for loop 
for l in range(3):
    for m in range(3):
        print("*",end=" ")
    print()
print("program ended\n")

#left downside triangle of * using for loop
for n in range(1,6):
    for o in range(n):
        print("*",end=" ")
    print()
print("program ended\n")

#left upside triangle of * using for loop
for p in range(5,0,-1):
    for q in range(p):
        print("*",end=" ")
    print()
print("program ended\n")

